    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String sql1 = "SELECT * FROM issuebook WHERE bookid = ?";
        try {
            pst = con.prepareStatement(sql1);
            pst.setString(1, jTextField1.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                String add1 = rs.getString("bookname");
                rbname.setText(add1);
                String add2 = rs.getString("bookauthor");
                rbauthor.setText(add2);
                String add3 = rs.getString("bookedition");
                rbedition.setText(add3);
                String add8 = rs.getString("studentid");
                RSid.setText(add8);
                String add4 = rs.getString("studentname");
                rsname.setText(add4);
                String add5 = rs.getString("yearandsection");
                ryearandsection.setText(add5);
                String add6 = rs.getString("course");
                rcourse.setText(add6);
                String add7 = rs.getString("semester");
                rsemester.setText(add7);
                String add9 = rs.getString("issuedate");
                issuedate = add9;
                
                rs.close();
                pst.close();
            }else{
                JOptionPane.showMessageDialog(this, "Issued Book or Student not found.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

