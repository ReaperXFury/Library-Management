    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username;
        String password;
        
        username = txtloginusername.getText();
        password = txtloginpass.getText();
        
        String queryLogIn = "SELECT * FROM student WHERE username = '" + username + "' AND password = '" + password + "'";
        
        try {
            pst = con.prepareStatement(queryLogIn);
            ResultSet rs = pst.executeQuery();
            if(!rs.next()){
                JOptionPane.showMessageDialog(null, "Invalid Username or Password");
            }else{
                JOptionPane.showMessageDialog(null, "Login successful.");
                txtloginusername.setText("");
                txtloginpass.setText("");
                StudentMainFrame smf = new StudentMainFrame();
                smf.setVisible(true);
                this.dispose();
            };
        } catch (SQLException ex) {
            Logger.getLogger(LogIn.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

